void performance_test(int argc, char **argv)
{
    char *cmd, *fun;
    rt_base_t level = 0;
    uint32_t i, size, loop, *dst = NULL, *src = NULL;

    if (argc < 2)
        goto out;

    level = rt_hw_interrupt_disable();
    cmd = argv[1];
    if (!rt_strcmp(cmd, "code"))
    {
        fun = argv[2];
        for (i = 0; i < HAL_ARRAY_SIZE(data_fun_group); i++)
        {
            if (!rt_strcmp(fun, code_fun_group[i].tag))
            {
                loop = (uint32_t)atoi(argv[3]);
                CodePerformance(&code_fun_group[i], loop);
                break;
            }
        }
        if (i >= HAL_ARRAY_SIZE(data_fun_group))
        {
            CodePerformanceAll();
        }
    }
    else if (!rt_strcmp(cmd, "data"))
    {
        fun = argv[2];
        for (i = 0; i < HAL_ARRAY_SIZE(data_fun_group); i++)
        {
            if (!rt_strcmp(fun, data_fun_group[i].tag))
            {
                dst = (uint32_t *)atoi(argv[3]);
                src = (uint32_t *)atoi(argv[4]);
                size = (uint32_t)atoi(argv[5]);
                DataAccess(dst, src, &data_fun_group[i], size);
                break;
            }
        }
        if (i >= HAL_ARRAY_SIZE(data_fun_group))
        {
            dst = (uint32_t *)atoi(argv[2]);
            src = (uint32_t *)atoi(argv[3]);
            DataAccessAll(dst, src);
        }
    }
    else if (!rt_strcmp(cmd, "print"))
    {
        fun = argv[2];
        if (!rt_strcmp(fun, "info"))
        {
            printf_info = 1;
        }
        else
        {
            printf_info = 0;
        }
    }
    else
    {

        goto out;
    }
    rt_hw_interrupt_enable(level);

    return;
out:
    rt_hw_interrupt_enable(level);

    performance_test_show_usage();
    return;
}

