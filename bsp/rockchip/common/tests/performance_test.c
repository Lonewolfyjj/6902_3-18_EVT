void CodePerformance(struct CODE_FUN_ITEM *item, uint32_t loop)
{
    uint64_t timCount[2], gap;
    struct CACHE_PMU_CNT pmuStatus[2];
    uint32_t us;

    PF_DBG_RESULT("Code Performance %s loop %lu\n", item->tag, loop);
    HAL_ICACHE_EnablePMU();
    HAL_ICACHE_Invalidate();
    timCount[0] = HAL_TIMER_GetCount(SYS_TIMER);
    HAL_ICACHE_GetPMU(&pmuStatus[0]);
    NopTestNKLoopN(item->f, loop);
    HAL_ICACHE_GetPMU(&pmuStatus[1]);
    timCount[1] = HAL_TIMER_GetCount(SYS_TIMER);

    PF_DBG_INFO("start timer count=");
    PF_DBG_RESULT("%ld%ld\n", (uint32_t)(timCount[0] >> 32), (uint32_t)(timCount[0]));
    PF_DBG_INFO("end timer count=");
    PF_DBG_RESULT("%ld%ld\n", (uint32_t)(timCount[1] >> 32), (uint32_t)(timCount[1]));
    PF_DBG_INFO("PMU read hit=");
    PF_DBG_RESULT("%lu\n", pmuStatus[1].rdHit - pmuStatus[0].rdHit);
    PF_DBG_INFO("PMU read totle=");
    PF_DBG_RESULT("%lu\n",  pmuStatus[1].rdNum - pmuStatus[0].rdNum);
    PF_DBG_INFO("PMU read miss penalty=");
    PF_DBG_RESULT("%lu\n", pmuStatus[1].rdMissPenalty - pmuStatus[0].rdMissPenalty);

    if (timCount[1] > timCount[0])
        gap = timCount[1] - timCount[0];
    else
        gap = timCount[0] - timCount[1];
    us = HAL_DivU64(gap, PLL_INPUT_OSC_RATE / 1000000);
    PF_DBG_INFO("cost(us) ");
    PF_DBG_RESULT("%lu\n", us);

    HAL_ICACHE_DisablePMU();
}

