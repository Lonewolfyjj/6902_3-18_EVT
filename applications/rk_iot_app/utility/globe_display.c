#include <rtthread.h>
#include <stdio.h>
#include <stdlib.h>

#include <string.h>

#ifdef RT_USING_IOT_DISPLAY
#include "globe_display.h"

#define DBG_LEVEL               DBG_LOG
#define DGB_SECTION_NAME        "DISPLAY"
#include "rkdebug.h"

display_callback display_cb;

void rtos_display_callback_register(display_callback cb)
{
    if (cb)
        display_cb = cb;
}

void rtos_display_callback_unregister(void)
{
    if (display_cb)
        display_cb = NULL;
}

#endif
