static void DataAccessHelper(uint32_t *dstbuf, uint32_t *srcbuf, uint32_t (*f)(uint32_t *, uint32_t *, uint32_t), uint32_t buffer_size)
{
    uint64_t timCount[2], gap;
    struct CACHE_PMU_CNT pmuStatus[2];
    uint32_t us, ret;

    /* 1st test */
    HAL_DCACHE_CleanByRange((uint32_t)srcbuf, buffer_size);
    HAL_DCACHE_GetPMU(&pmuStatus[0]);
    timCount[0] = HAL_TIMER_GetCount(SYS_TIMER);
    ret = f(dstbuf, srcbuf, buffer_size);
    timCount[1] = HAL_TIMER_GetCount(SYS_TIMER);
    HAL_DCACHE_GetPMU(&pmuStatus[1]);

    PF_DBG_INFO("start timer count=");
    PF_DBG_RESULT("%ld%ld\n", (uint32_t)(timCount[0] >> 32), (uint32_t)(timCount[0]));
    PF_DBG_INFO("end timer count=");
    PF_DBG_RESULT("%ld%ld\n", (uint32_t)(timCount[1] >> 32), (uint32_t)(timCount[1]));
    PF_DBG_INFO("PMU read hit=");
    PF_DBG_RESULT("%lu\n", pmuStatus[1].rdHit - pmuStatus[0].rdHit);
    PF_DBG_INFO("PMU read totle=");
    PF_DBG_RESULT("%lu\n",  pmuStatus[1].rdNum - pmuStatus[0].rdNum);
    PF_DBG_INFO("PMU read miss penalty=");
    PF_DBG_RESULT("%lu\n", pmuStatus[1].rdMissPenalty - pmuStatus[0].rdMissPenalty);
    PF_DBG_INFO("PMU write hit=");
    PF_DBG_RESULT("%lu\n", pmuStatus[1].wrHit - pmuStatus[0].wrHit);
    PF_DBG_INFO("PMU write totle=");
    PF_DBG_RESULT("%lu\n",  pmuStatus[1].wrNum - pmuStatus[0].wrNum);
    PF_DBG_INFO("PMU write miss penalty=");
    PF_DBG_RESULT("%lu\n", pmuStatus[1].wrMissPenalty - pmuStatus[0].wrMissPenalty);
    if (timCount[1] > timCount[0])
        gap = timCount[1] - timCount[0];
    else
        gap = timCount[0] - timCount[1];
    us = HAL_DivU64(gap, PLL_INPUT_OSC_RATE / 1000000);
    PF_DBG_INFO("ret =%lx data size(byte)=", ret);
    PF_DBG_RESULT("%lu\n", buffer_size);
    PF_DBG_INFO("cost(us)=");
    PF_DBG_RESULT("%lu\n", us);
}

void DataAccess(uint32_t *dst, uint32_t *src, struct DATA_FUN_ITEM *item, uint32_t buffer_size)
{
    uint32_t *srcbuf, *dstbuf;

    HAL_DCACHE_EnablePMU();
    dstbuf = dst;
    srcbuf = src;
    if (!src && item->f != &MyMemSet)
        srcbuf = rt_malloc_align(buffer_size + CACHE_LINE_SIZE, CACHE_LINE_SIZE);
    if (!dst)
        dstbuf = rt_malloc_align(buffer_size + CACHE_LINE_SIZE, CACHE_LINE_SIZE);

    PF_DBG_RESULT("Data performance %s(%p, %p, %lu)\n", item->tag, dstbuf, srcbuf, buffer_size);

    rt_memset(dstbuf, 0x0, buffer_size);
    DataAccessHelper(dstbuf, srcbuf, item->f, buffer_size);
    if (src)
        PF_DBG_INFO("src[0]%lx src[1]%lx\n", srcbuf[0], srcbuf[1]);
    PF_DBG_INFO("dst[0]%lx dst[1]%lx\n", dstbuf[0], dstbuf[1]);

    if (!src && item->f != &MyMemSet)
        rt_free_align(srcbuf);
    if (!dst)
        rt_free_align(dstbuf);
    HAL_DCACHE_DisablePMU();
}

struct DATA_FUN_ITEM data_fun_group[] =
{
    {"memcpy", &MyMemCpy, 4096},
    {"sum", &MySum, 4096},
    {"memset", &MyMemSet, 4096},
};

void DataAccessAll(uint32_t *dst, uint32_t *src)
{
    uint32_t i;

    for (i = 0; i < HAL_ARRAY_SIZE(data_fun_group); i++)
    {
        DataAccess(dst, src, &data_fun_group[i], data_fun_group[i].size_recomand);
    }
}

